// Namespaces
/*
Namespaces
- feature present only in C++, NOT in C
- namespace don't have access modifiers
- provide the space where we can define/declare identifier,variable,methods,classes
- analogue/similar to packages in Java
- define a scope

With the help of namespaces, we avoid name collision
ex. we can have two different methods but with same name, every method in a different namespace

Syntax :

namespace namespace_name {

// methods
// classes
// variables
// etc


}


A namespace is used with directive "using"
ex: using namespace std;

std namespace = standard library in C++ =where all the classes,methods and templates are declared

*/
