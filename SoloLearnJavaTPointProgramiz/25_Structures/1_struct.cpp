// Struct in C++
// = are used for lightweight object like Rectangle, Circle .etc
// = are value type
// = collection of different data types (similar with classes)
/* Syntax:

struct struct_name{

    //member declaration
}

ex:
struct Student {

    char name[20];
    int id;
    int age;
}

Student s;  // s=variable of type Student (struct) ; s=instance of Student struct

-- for accesing a variable of structure we use  "." operator. ex: s.age


*/

// Struct vs Classes

/*

1.if access specifier not specified explicitly, by default  structure == public
1.if access specifier not specified explicitly, by default  class     == private

2. the instance of a structure = structure variable
2. the instance of a class     = object of a class

3. struct struct_name { // body of the structure }
3. class class_name { // body of the class }



*/
