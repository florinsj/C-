// Sololearn
// the "auto" keyword allows the system to automatically deduct the type of the variable
// when use "auto" keyword,is mandatory to initialize the var on same line with declaration
// ex : auto x=4;   // the system will deduct the type of x as integer

int main()
{

    auto a = 4;       // int
    auto b = 'v';     // char
    auto c = "hello"; // string
    auto d = 3.32;    // float
}
